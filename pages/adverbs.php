    <h2>Наречия в сербском языке</h2>
    
    <h3>Что такое наречия?</h3>
    <p>Наречия описывают <strong>как, где, когда</strong> происходит действие. Отвечают на вопросы: како? где? када?</p>

    <h3>Образование наречий от прилагательных</h3>
    <p>Большинство наречий образуются добавлением окончания <strong>-о</strong> к основе прилагательного:</p>
    
    <table>
        <thead>
            <tr>
                <th>Прилагательное</th>
                <th>Наречие</th>
                <th>Перевод</th>
            </tr>
        </thead>
        <tbody>
            <tr><td>леп (красивый)</td><td>лепо</td><td>красиво</td></tr>
            <tr><td>брз (быстрый)</td><td>брзо</td><td>быстро</td></tr>
            <tr><td>тих (тихий)</td><td>тихо</td><td>тихо</td></tr>
            <tr><td>добар (хороший)</td><td>добро</td><td>хорошо</td></tr>
            <tr><td>лош (плохой)</td><td>лоше</td><td>плохо</td></tr>
            <tr><td>лак (лёгкий)</td><td>лако</td><td>легко</td></tr>
            <tr><td>тежак (тяжёлый)</td><td>тешко</td><td>тяжело</td></tr>
        </tbody>
    </table>

