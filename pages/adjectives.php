    <h4>Превосходная степень</h4>
    <p>Образуется путём прибавления префикса <strong>нај- (naj-)</strong> к форме сравнительной степени:</p>
    <ul>
        <li>близак (близкий) → ближи (ближе) → <strong>најближи</strong> (самый близкий)</li>
        <li>висок (высокий) → виши (выше) → <strong>највиши</strong> (самый высокий)</li>
        <li>добар (хороший) → бољи (лучше) → <strong>најбољи</strong> (самый лучший)</li>
    </ul>

