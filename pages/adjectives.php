    <h3>Вопрос о цвете</h3>
    <p><strong>Које је боје? (Koje je boje?)</strong> - Какого цвета?</p>
    <ul>
        <li>Које је боје твој ауто? - Какого цвета твоя машина?</li>
        <li>Мој ауто је плаве боје. - Моя машина синего цвета.</li>
    </ul>
