    <h3>Склонение прилагательных</h3>
    <p>Прилагательные склоняются по падежам. Основные падежи:</p>
    <ul>
        <li><strong>Номинатив</strong> (именительный) - ко? шта?</li>
        <li><strong>Генитив</strong> (родительный) - кога? чега?</li>
        <li><strong>Датив</strong> (дательный) - коме? чему?</li>
        <li><strong>Акузатив</strong> (винительный) - кога? шта?</li>
        <li><strong>Инструментал</strong> (творительный) - с ким? чим?</li>
        <li><strong>Локатив</strong> (предложный) - о коме? о чему?</li>
    </ul>

    <h3>Степени сравнения прилагательных</h3>
    
    <h4>Образование сравнительной степени</h4>
    <p>Сравнительная степень образуется путём прибавления окончаний к неопределённой форме мужского рода:</p>
    
    <table>
        <thead>
            <tr>
                <th>Тип прилагательного</th>
                <th>Мужской род</th>
                <th>Женский род</th>
                <th>Средний род</th>
                <th>Пример</th>
            </tr>
        </thead>
        <tbody>
            <tr>
                <td>Многосложные</td>
                <td>-иjи (-iji)</td>
                <td>-иjа (-ija)</td>
                <td>-иjе (-ije)</td>
                <td>интересан → интересантиjи</td>
            </tr>
            <tr>
                <td>Односложные</td>
                <td>-jи (-ji)</td>
                <td>-jа (-ja)</td>
                <td>-jе (-je)</td>
                <td>стар → стариjи</td>
            </tr>
            <tr>
                <td>Особые случаи</td>
                <td>-ши (-ši)</td>
