    <h3>Как говорить время (Како се каже време/Kako se kaže vreme)</h3>

    <h4>Основные правила</h4>
    <ul>
        <li><strong>Колико је сати?</strong> (Koliko je sati?) - Который час?</li>
        <li><strong>Сад је...</strong> (Sad je...) - Сейчас...</li>
        <li>Для времени до 30 минут используется конструкция "час + минуты"</li>
        <li>Для времени после 30 минут используется конструкция "без (минут) + следующий час"</li>
    </ul>

