    <h2>Служебные слова в сербском языке</h2>
    
    <div class="particles-intro">
        <p>Служебные слова - это предлоги, союзы, частицы и междометия, которые не имеют самостоятельного значения, но играют важную роль в построении предложений и выражении различных оттенков смысла.</p>
    </div>

