            </thead>
            <tbody>
                <tr>
                    <td><strong>o</strong></td>
                    <td>о</td>
                    <td>удивление, обращение</td>
                    <td>O, kako lepo! (О, как красиво!)</td>
                </tr>
                <tr>
                    <td><strong>ah</strong></td>
                    <td>ах</td>
                    <td>восхищение, сожаление</td>
                    <td>Ah, kako je lepo! (Ах, как красиво!)</td>
                </tr>
                <tr>
                    <td><strong>oh</strong></td>
                    <td>ох</td>
                    <td>удивление, восхищение</td>
                    <td>Oh, ne verujem! (Ох, не верю!)</td>
                </tr>
                <tr>
                    <td><strong>eh</strong></td>
                    <td>эх</td>
                    <td>сожаление</td>
