    <div class="particles-section">
        <h3>Междометия (Uzvisi)</h3>
        <p>Междометия выражают чувства и эмоции, но не называют их.</p>
        
        <table class="numbers-table">
            <thead>
                <tr>
                    <th>Междометие</th>
                    <th>Значение</th>
                    <th>Эмоция</th>
