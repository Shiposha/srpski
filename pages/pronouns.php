        <div class="pronouns-explanation">
            <h4>Пояснение к личным местоимениям:</h4>
            <p>В некоторых падежах есть полная и краткая формы местоимений. В большинстве случаев используется краткая форма, например:</p>
            <ul>
                <li><em>Ne znam ga.</em> (Я его не знаю.)</li>
                <li><em>Hvala ti.</em> (Спасибо тебе.)</li>
                <li><em>Pisala sam joj.</em> (Я написала ей.)</li>
            </ul>
            
            <p><strong>Полная форма используется в следующих случаях:</strong></p>
            <ol>
                <li>В абсолютном начале предложения.<br>
                    <em>Njega ne znam.</em> (Его я не знаю.)</li>
                <li>После предлогов.<br>
                    <em>Idite bez mene.</em> (Идите без меня.)</li>
                <li>При наличии логического ударения.<br>
                    <em>Pišem tebi, a ne njoj.</em> (Пишу тебе, а не ей.)</li>
                <li>После союзов <strong>a, i, ili, nego</strong> и перед ними.<br>
                    <em>Daj knjigu njemu ili njoj.</em> (Дай книгу ему или ей.)<br>
                    <em>Sve si pozvao, a mene nisi pozvao.</em> (Всех ты позвал, а меня не позвал.)<br>
                    <em>Tebe volim i nju volim.</em> (Тебя люблю и её люблю.)</li>
            </ol>
            
            <p><strong>Особенности местоимения "ona":</strong><br>
            В Akuzativ у местоимения <strong>ona</strong> есть 2 краткие формы – <strong>ju</strong> и <strong>je</strong>. В большинстве случаев используется <strong>je</strong>, например:<br>
            <em>Ja sam je znala.</em> (Я её знала.)<br>
            Форма <strong>ju</strong> используется в прошедшем времени при наличии глагола быть в форме <strong>je</strong>.<br>
            <em>On ju je znao.</em> (Он её знал.)</p>
        </div>
