    <h2>Существительные в сербском языке (Именице/Imenice)</h2>
    
    <h3>Основные характеристики</h3>
    <p>Сербские существительные имеют:</p>
    <ul>
        <li><strong>Три рода</strong> — мужской, женский и средний</li>
        <li><strong>Два числа</strong> — единственное (једнина/jednina) и множественное (множина/množina)</li>
        <li><strong>Семь падежей</strong> — именительный, родительный, дательный, винительный, звательный, творительный, предложный</li>
    </ul><br>

