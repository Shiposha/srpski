    <h5>Ключевые особенности и подсказки:</h5>
    <ul>
        <li>зная форму глагола в «он», можем восстановить всю цепочку <br>
            <em>Он чита => ја читам + ти читаш + ми читамо + ви читате</em></li>
        <li>А как определить форму глагола в «они»? Смотрим на форму глагола в «он»!</li>
            <em>Он чита => группа А => они читају (а => ају)</em><br>
            <em>Он ради => группа И => они раде (и => е)</em><br>
            <em>Он пише => группа Е => они пишу (е => у)</em><br>
        <li>Чтобы определить группу, найдите инфинитив и посмотрите на его окончание, а для проверки — поставьте глагол в форму 3-го лица множественного числа.</li>
        <li>Самая большая группа — E-группа. В ней чаще всего встречаются чередования согласных (писати → пишем, летети → летим).</li>
        <li>Глаголы-исключения: Некоторые частые глаголы не подчиняются общим правилам. Их нужно запоминать:
            <ul>
                <li>моћи (мочь) → могу, можеш, може...</li>
                <li>ићи (идти) → идем, идеш, иде... (хотя оканчивается на -ити)</li>
                <li>хтети (хотеть) → в настоящем времени используется Futur I: хоћу, хоћеш, хоће...</li>
            </ul>
        </li>
    </ul>
    <br>

    <h5>Особенности спряжения в 3-м лице множественного числа:</h5>
    <ul>
        <li><strong>A-группа:</strong> окончание <strong>-ју</strong> (читати → читају)</li>
        <li><strong>I-группа:</strong> окончание <strong>-е</strong> (радити → раде)</li>
        <li><strong>E-группа:</strong> имеет несколько вариантов окончаний:
            <ul>
                <li><strong>-у</strong> (самое частное): пити → пију, писати → пишу</li>
                <li><strong>-е</strong>: носити → носе, возити → возе</li>
                <li><strong>-ју</strong>: певати → певају, играти → играју</li>
            </ul>
        </li>
    </ul>
    <br>

