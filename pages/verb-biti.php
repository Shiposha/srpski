            <tr><td>Он/Она/Оно</td><td>ће</td><td>Он ће читати</td><td>Он будет читать</td></tr>
            <tr><td>Мы</td><td>ћемо</td><td>Ми ћемо читати</td><td>Мы будем читать</td></tr>
