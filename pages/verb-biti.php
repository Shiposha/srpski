            </tr>
        </thead>
        <tbody>
            <tr><td>Я</td><td>ja sam</td><td>ja <strong>nisam</strong></td><td>я не есть / меня нет</td></tr>
            <tr><td>Ты</td><td>ti si</td><td>ti <strong>nisi</strong></td><td>ты не есть / тебя нет</td></tr>
            <tr><td>Он/Она/Оно</td><td>on/ona/ono je</td><td>on/ona/ono <strong>nije</strong></td><td>он/она/оно не есть / его/её нет</td></tr>
            <tr><td>Мы</td><td>mi smo</td><td>mi <strong>nismo</strong></td><td>мы не есть / нас нет</td></tr>
            <tr><td>Вы</td><td>vi ste</td><td>vi <strong>niste</strong></td><td>вы не есть / вас нет</td></tr>
            <tr><td>Они</td><td>oni/one/ona su</td><td>oni/one/ona <strong>nisu</strong></td><td>они не есть / их нет</td></tr>
        </tbody>
    </table>
    <p><strong>Пример:</strong> Ja <strong>nisam</strong> студент. — Я <strong>не</strong> студент.</p>

    <h5>Отрицание в других временах</h5>
    <ul>
        <li><strong>Будущее время (Футур I):</strong> Ja <strong>neću</strong> raditi. (Я не буду работать.)</li>
        <li><strong>Прошедшее время:</strong> Ja <strong>nisam</strong> radio/la. (Я не работал/а.)</li>
        <li><strong>Повелительное наклонение:</strong> <strong>Nemoj</strong> raditi! (Не работай!)</li>
    </ul>
