    <h5>Отрицательная форма Футур I</h5>
    <ul>
        <li>Ja <strong>нећу</strong> читати (Я не буду читать)</li>
        <li>Ti <strong>нећеш</strong> читати (Ты не будешь читать)</li>
        <li>On <strong>неће</strong> читати (Он не будет читать)</li>
    </ul><br>

    <!-- БУДУЩЕЕ ВРЕМЯ II - КРАТКО -->
    <h4>4. Будущее время II (Футур II)</h4>
    <p><em>Используется реже, для завершенных действий в будущем</em></p>
    <p>Образуется: ћу бити + причастие</p>
    <ul>
        <li>Ја <strong>ћу бити написао/ла</strong> (Я буду написавший/ая)</li>
        <li>Используется после: <strong>кад, ако, чим</strong></li>
        <li>Пример: <strong>Позваћу те чим будем стигао</strong> (Позову тебя, как только прибуду)</li>
    </ul><br>

	<h4>5. Плюсквамперфект</h4>
	<p><em>Редко используется, в основном в литературе. Действие, которое произошло до другого прошлого действия.</em></p>
	<p><strong>Образование:</strong> причастие + BITI в прошедшем времени</p>
	<p><strong>Пример:</strong> Ја сам био видео / била видела (Я уже видел/а до того как...)</p>
	<p><em>В разговорной речи обычно заменяется перфектом</em></p><br>

